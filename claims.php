			<h3>RCV</h3>

			<p>The dollar amount needed to replace damaged personal property or dwelling property without deducting for depreciation but may be limited by the maximum dollar allowed by your policy.
			</p>
			<h3>ACV</h3>

			<p>Cost of replacing damaged or destroyed property with comparable new property, minus depreciation and obsolescence.
			</p>
			<h3>ACV Settlement</h3>

			<p>The replacement cost value less the depreciation and the deuctible. Typcially this is the intial check you will receive from your insruance carrier.
			</p>
			<h3>Deprecation</h3>

			<p>The decrease in value of an item due to wear, tear, or decay over time.
			</p>
			<h3>Recoverable Deprecation</h3>

			<p>The amount held by the insurance carrier that is only able to be recovered or reimbursed to the homeowner if those expenses have actually been incurred. An itemized receipt, from the contractor, for all work completed is often required before any depreciation is released. The insurance company may also require a visual inspection of all completed work before realsing the depreciation check. 
			</p>
			<h3>Deductible</h3>

			<p>The amount for which the insured (you) is liable on each loss.
			</p>
			<h3>Personal Property</h3>

			<p>All tangible property that is either temporary or movable in some way, such as furniture, jewlery, electronics, etc…
			</p>
			<h3>Renters Insurance</h3>

			<p>A form of insurance that covers a policyholder’s belongings against perils. It also provides personal liability coverage and additional living expenses. Possessions can be covered for their replacement cost or the actual cash value, which includes depreciation.
