						<h2>Mold</h2>

						<p>When excessive moisture accumulates in buildings or on building materials, mold growth will often occur, particularly if the moisture problem remains undiscovered or unaddressed.</p>

						<h3>Preventing Mold</h3>
						<ul>
							<li>The key to mold control is moisture control</li>
							<li>Fix leaks and seepage as soon as you notice the issue</li>
							<li>Put a plastic cover over dirt in crawl spaces to prevent moisture from coming in from the ground</li>
							<li>Use exhaust fans in bathrooms and kitchens to remove moisture to the outside</li>
							<li>Vent your clothes dryer to the outside</li>
							<li>Turn off certain appliances (such as humidifiers or kerosene heaters) if you notice moisture on windows and other surfaces</li>
							<li>Use dehumidifiers and air conditioners, especially in hot, humid climates, to reduce moisture in the air</li>
							<li>Raise the temperature of cold surfaces where moisture condenses</li>
							<li>Use insulation or storm windows</li>
							<li>Increase air circulation by using fans and by moving furniture from wall corners to promote air and heat circulation and keep doors open between rooms</li>
							<li>Carpet on concrete floors can absorb moisture and serve as a place for biological pollutants to grow - use area rugs which can be taken up and washed often</li>
							<li>Dry water-damaged areas and items within 24-48 hours to prevent mold growth</li>
						</ul>
