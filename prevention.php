						<ul>
							<li>Keep your home free of oily rags and trash build-up. Gasoline and other flammable liquids should be stored in approved metal safety cans.</li>
							<li>Cleaning materials should be kept in a ventilated area, away from any heat source. Vapors given off by these substances can ignite when they come in contact with a heat source, such as a pilot light.</li>
