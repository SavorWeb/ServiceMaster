							<li>Remove all weeds, tree branches and litter from your yard.</li>
							<li>If you have a fireplace, make sure it is properly screened and install a spark arrestor with at least a half-inch mesh on the chimney.</li>
							<li>Daily household trash should be kept in a covered can away from any heat source. Recycle newspapers frequently.</li>
							<li>Be a careful cook. Never wear long sleeves when you cook -- they can catch fire. Keep the handles of your pots turned inward, so the pots can't be knocked over. Never put foil or other metals in a microwave oven.</li>
