					<h3>Frozen Pipes</h3>
					<p>
						The water inside pipes can freeze when outdoor temperatures drop below freezing. As freezing water expands, it causes the pressure inside the pipes to increase, possibly leading to bursting pipes. Here are some tips for preventing frozen pipes:
					</p>

					<ul>
						<li>Insulate pipes, especially those close to outside walls, attics or crawl spaces where the chance of freezing is greatest</li>
						<li>Seal air leaks surrounding or near pipes</li>
						<li>Keep garage doors closed if there are water supply lines in the garage</li>
						<li>Disconnect all outdoor hoses and turn off water to exterior faucets and sprinkler systems</li>
						<li>Open kitchen and bathroom cabinet doors to allow warmer air to circulate around the plumbing</li>
						<li>Keep heat at 55 degrees F. or higher even when you are out of town</li>
						<li>During a cold spell turn on both hot and cold faucets near outside walls to allow a small trickle of water to run during the night</li>
						<li>If you need to be away from home, leave the heat on and drain your water system before you go</li>
						<li>Identify the locations of shutoff valves so that you are prepared to stop the flow of water as soon as possible when a pipe bursts</li>
					</ul>
				</div>


				<div class="fourth"><h3>Ice Dams</h3>
					<p>
						Ice dams form when melting ice and snow refreeze above the eaves of your roof and subsequent melting backs up under the shingles. This causes interior leaks and water damage to interior walls and ceilings.
					</p>

					<p>
						Proper ventilation, drainage and insulation are the only ways to prevent ice dams and can be achieved in the following ways:
					</p>

					<ul>
						<li>Make sure your gutters are clear of leaves and debris</li>
						<li>Check and seal places where warm air could leak from your house to the attic: vent pipes, exhaust fans, chimneys, attic hatches and light fixtures are all possibilities</li>
						<li>Inspect, or have your roof and attic inspected for proper ventilation and insulation</li>
						<li>Look for signs of inadequate ventilation: rust spots, rusty nails or a mildew smell are all signals that moisture has formed on the inside of your roof</li>
						<li>If you have soffit vents in your eaves, make sure they are not blocked and insulation surrounding them is secured so that air can flow easily</li>
						<li>Keep snow from accumulating on the lower three to six feet of your roof</li>
						<li>Install snow and ice slides to prevent ice and snow from "bonding" to the lower roof</li>
						<li>Install a rubberized ice and water shield beneath the roof shingles for the first three to six feet from the eaves up</li>
						<li>Install heating cable along the eaves to melt ice</li>
					</ul>
				</div>

				<div class="fourth">
					<h3>Water Backup</h3>
					<p>
						Water backup occurs when the water around the foundation of your home exceeds the capacity of removal systems to drain it. It can occur from surface water seeping into foundation walls or sewer systems overflowing up through drains in the basement. Below we have listed some tips for keeping your home safe from water backup.
					</p>

					<ul>
						<li>Maintain gutters and downspouts, keeping them free of debris and leaves and repairing them if they are sagging</li>
						<li>Inspect your gutters' capacity - after fifteen minutes of heavy rain, if water overflows the gutters, install additional downspouts</li>
						<li>Extend downspouts at least ten feet away from the house</li>
						<li>Adjust landscaping and irrigation so that water flows away from the foundation</li>
						<li>Drain subsurface groundwater and storm water with a sump pump system that has battery backup and replacement warnings</li>
						<li>Run your sump pump every few months and clean it annually before the rainy season</li>
						<li>Prevent backflow of sanitary sewer water by installing backflow valves and standpipes at all basement drain locations, including sinks and toilets</li>
						<li>Prepare your basement "just in case" by raising your washer, dryer, water heater, oil tank, furnace, all electrical wiring and personal items above typical water backup levels.</li>
					</ul>
