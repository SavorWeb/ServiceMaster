<div class="third"><h2>IICRC</h2>
<p>Institute of Inspection Cleaning and Restoration Certification (IICRC) Certified Firms have earned the right to display the IICRC logo as a symbol of quality. In order to achieve IICRC-certified status, firms must meet a rigorous list of standards in business ethics and expertise. All IICRC Certified Firms must:</p>
<ul>
	<li>Present accurate information to consumers and conduct business with honesty and integrity.</li>
	<li>Require a technician on all jobs who has been formally trained and passed all required tests.</li>
	<li>Require a continuing education program to keep technicians up-to-date on the latest changes in the industry.</li>
	<li>Maintain liability insurance to protect all parties in the event of an accident.</li>
	<li>Maintain a written complaint policy and agree to Better Business Bureau or similar arbitration to resolve disputes, and accept the conclusions and recommendations of arbitration.</li>
</ul>
</div>
