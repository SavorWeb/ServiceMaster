<h1>About ServiceMaster of North Idaho</h1>
<p>ServiceMaster of North Idaho (SMNI) was begun by Val Hansen over 40 years ago in a garage in the Coeur d’Alene area. SMNI successfully performed disaster restoration work for the rapidly growing Kootenai County area under the tutelage of Ms. Hansen. In 1999, ServiceMaster was bought by Mike MacRae, who had a vision to extend SMNI’s coverage area into the Sandpoint and Spokane markets. In 2007, Kelly Norton joined the team here at SMNI, and in 2013, he, along with John Knight, purchased the company. </p>
