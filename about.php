<h2>QRV </h2>
<p>Established in 1991, the ServiceMaster Restore QRV Network has been a solution for insurance industry professionals in identifying franchisees committed to providing superior, consistent disaster mitigation services. In order to earn and maintain QRV certification, firms must:</p>
<ul>
	<li>Demonstrate the ability to competently deliver every aspect of the ServiceMaster Restore service guarantees, including accessibility, response time, communications capabilities, quality results, and conscientious safety controls.</li>
	<li>Employ the utmost integrity in estimating and billing practices.</li>
	<li>Complete, and commit to continue, employee training programs that keep ServiceMaster Restore on the cutting edge of effective restoration techniques, products, and technology.</li>
	<li>Use the proven ServiceMaster Restore equipment, processes, and products.</li>
	<li>Perform all services in compliance with ServiceMaster Restore specifications.</li>
	<li>Meet or exceed the ServiceMaster Restore requirements for General Liability Insurance and Workers’ Compensation Insurance.</li>
	<li>Resolve customer service issues swiftly and professionally. Guaranteed.</li>
	<li>Commit to the ServiceMaster Restore principles of environmental stewardship.</li>
</ul>
