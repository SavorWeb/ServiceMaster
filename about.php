<h1>Company History</h1>
<p> ServiceMaster was founded in Chicago in 1929 as a moth-proofing company by Marion E. Wade, a former minor league baseball player. Wade’s company was incorporated in 1947 and expanded its offerings to include carpet cleaning in 1952, establishing one of America's first franchise businesses. In 1962, the company took its expertise to the health care arena and was a pioneer in third-party outsourcing, known at the time as management services. In 1980, the company began serving the education market and expanded its offerings to include a broader range of commercial services.</p>

<p>In the 1980s, the company experienced further expansion of the consumer segment of the business. During this time, ServiceMaster built on its original franchise business to form a network of high-quality branch- and franchise-based service companies – including Terminix, TruGreen and Merry Maids – and other category-leading commercial and residential service brands, including American Home Shield. In 2001, ServiceMaster divested its management services businesses to focus more on its residential and commercial business.</p>

<p>In July 2007, a group of equity sponsors led by Clayton, Dubilier and Rice, Inc. (CD&R) acquired ServiceMaster and took the company private. Also in 2007, the company moved its headquarters from Downers Grove, Illinois, to Memphis, Tennessee, to better enable the corporate staff to work side by side with all of its brand teams. Robert J. Gillette was appointed chief executive officer in June 2013. In January 2014, TruGreen separated from ServiceMaster to operate as a standalone company within the CD&R portfolio.</p>
<p>Today, the company operates under the same elements of Wade’s original purpose – to deliver exceptional service and quality to customers – while adhering to an enduring set of values that demands all employees act with the highest level of ethics and integrity in their interactions with customers, suppliers and each other.
With a global network of more than 6,900 company-owned, franchise and licensed locations, Memphis-based ServiceMaster is one of the world’s largest residential and commercial service networks.  Through approximately 13,000 corporate associates and a franchise network that independently employs an estimated 31,000 additional people, the ServiceMaster family of brands provided services and products to approximately 5 million customers during the year. ServiceMaster consistently provides an unparalleled level of service by maintaining the fastest response time in the industry, committing to the highest level of training and technology, and keeping the interests of our customers at the forefront.</p>
